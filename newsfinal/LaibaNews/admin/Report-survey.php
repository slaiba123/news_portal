<?php
// Calculate average rating per question
$question_ratings = [];
foreach ($survey_data as $data) {
    $question = $data['question_text'];
    $rating = (int)$data['response_text'];
    
    if (!isset($question_ratings[$question])) {
        $question_ratings[$question] = ['total' => 0, 'count' => 0];
    }
    
    $question_ratings[$question]['total'] += $rating;
    $question_ratings[$question]['count']++;
}

$average_ratings = [];
foreach ($question_ratings as $question => $data) {
    $average_ratings[$question] = $data['total'] / $data['count'];
}
