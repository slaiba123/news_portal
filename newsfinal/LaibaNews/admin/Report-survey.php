$query = "SELECT sq.question_text, sr.response_text
          FROM survey_question sq
          JOIN survey s ON sq.question_id = s.question_id
          JOIN survey_response sr ON sr.response_id = s.response_id";
$result = mysqli_query($con, $query);

$survey_data = [];
if ($result && mysqli_num_rows($result) > 0) {
    while ($row = mysqli_fetch_assoc($result)) {
        $survey_data[] = $row;
    }
}
